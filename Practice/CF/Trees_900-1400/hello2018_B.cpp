string solve(vector<int>graph[], int n)
{
	int leaf_count = 0;
	for (int i = 1; i < n; i++)
	{
		leaf_count = 0;
		for (int a : graph[i])
		{
			if (graph[a].size() == 0)
				leaf_count++;
		}

		// Either the node is a leaf node or it's leaf count should be greater than or equal to 3.
		// if that's not the case then return "NO"
		if (leaf_count < 3 and graph[i].size() != 0)
			return "NO";
	}
	return "YES";
}

int main()
{
	int n;
	cin >> n;
	vector<int>graph[n + 1];

	int x;
	for (int i = 1; i < n; i++)
	{
		cin >> x;
		graph[x].push_back(i + 1);
	}

	cout << solve(graph, n);
}

